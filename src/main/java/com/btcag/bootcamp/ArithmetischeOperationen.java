package com.btcag.bootcamp;

import java.util.Scanner;

public class ArithmetischeOperationen {
    public static void main(String[] args) {
       int var = 1;

       System.out.println("Die Variable ist 1. Mit var++ wird diese erst ausgegeben und danach um 1 erhöht. Guck:" + var++);
        System.out.println("Die Variable ist nun aber 2. Mit ++var wird diese erst um 1 erhöht und danach ausgegeben. Guck:" + ++var);
    }
}
